DATA SEGMENT
    NUM DW 5
    FACTORIAL DW ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, 1
    MOV CX, NUM

FACTORIAL_LOOP:
    MUL CX
    DEC CX
    JNZ FACTORIAL_LOOP

    MOV FACTORIAL, AX

    MOV AH, 4CH
    INT 21H

CODE ENDS
END START